public class UIDataBean {

	String source;
	String destination;
	String delay;
	String prefix;
	String separator;

	String generatorFromDate;
	String generatorToDate;
	String generatorCommentValue;
	String generatorRole;
	String generatorDefaultComment;

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getDelay() {
		return delay;
	}

	public void setDelay(String delay) {
		this.delay = delay;
	}

	public String getPrefix() {
		return prefix;
	}

	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}

	public String getSeparator() {
		return separator;
	}

	public void setSeparator(String separator) {
		this.separator = separator;
	}

	public String getGeneratorFromDate() {
		return generatorFromDate;
	}

	public void setGeneratorFromDate(String generatorFromDate) {
		this.generatorFromDate = generatorFromDate;
	}

	public String getGeneratorToDate() {
		return generatorToDate;
	}

	public void setGeneratorToDate(String generatorToDate) {
		this.generatorToDate = generatorToDate;
	}

	public String getGeneratorCommentValue() {
		return generatorCommentValue;
	}

	public void setGeneratorCommentValue(String generatorCommentValue) {
		this.generatorCommentValue = generatorCommentValue;
	}

	public String getGeneratorRole() {
		return generatorRole;
	}

	public void setGeneratorRole(String generatorRole) {
		this.generatorRole = generatorRole;
	}

	public String getGeneratorDefaultComment() {
		return generatorDefaultComment;
	}

	public void setGeneratorDefaultComment(String generatorDefaultComment) {
		this.generatorDefaultComment = generatorDefaultComment;
	}

}
